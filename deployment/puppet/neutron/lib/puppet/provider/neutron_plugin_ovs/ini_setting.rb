Puppet::Type.type(:neutron_plugin_ovs).provide(
  :ini_setting,
  :parent => Puppet::Type.type(:ini_setting).provider(:ruby)
) do

  def section
    resource[:name].split('/', 2).first
  end

  def setting
    resource[:name].split('/', 2).last
  end

  def separator
    '='
  end

  def file_path
    '/etc/neutron/plugins/openvswitch/ovs_neutron_plugin.ini'
  end

end
